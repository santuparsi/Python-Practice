##  Variables:-
##
##  Variables are assigned objects (number, strings, lists, dataframes)
##  Think of variable as a box filled with an object as a similar process to assigning a variable


num1, num2 = 5, 3

print(num1, num2)

# Mathematical Operations

print("Addition: ")
print(num1 + num2)

print("Substraction: ")
print(num1 - num2)

print("Multiplication: ")
print(num1 * num2)

print("Power: ")
print(num1 ** num2)

print("Division: ")
print(num1 / num2)

print("Floor Division: ")
print(num1 // num2)

print("Modulus: ")
print(num1 % num2)

# Type Casting

num1 = float(num1)
num2 = float(num2)
print("Data Type of variable 'num1' is " + str(type(num1)))
print("Data Type of variable 'num2' is " + str(type(num2)))
print(num1, num2, sep=", ")
